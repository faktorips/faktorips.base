import org.faktorips.devtools.model.versionmanager.IIpsProjectMigrationOperationFactory;

public class Migration_24_1_1 extends Migration_24_1_0 {

    private static final String VERSION = "24.1.1"; //$NON-NLS-1$

    public Migration_24_1_1(IIpsProject projectToMigrate, String featureId) {
        super(projectToMigrate, featureId, VERSION);
    }

    public static class Factory implements IIpsProjectMigrationOperationFactory {
        @Override
        public AbstractIpsProjectMigrationOperation createIpsProjectMigrationOpertation(IIpsProject ipsProject,
                String featureId) {
            return new Migration_24_1_1(ipsProject, featureId);
        }
    }
}
