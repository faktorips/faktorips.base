package org.faktorips.devtools.htmlexport;

import org.eclipse.jface.resource.ImageDescriptor;
import org.eclipse.ui.plugin.AbstractUIPlugin;
import org.osgi.framework.BundleContext;

/**
 * The activator class controls the plug-in life cycle
 */
public class HtmlExportPlugin extends AbstractUIPlugin {

    public static final String PLUGIN_ID = "org.faktorips.devtools.htmlexport"; //$NON-NLS-1$

    private static HtmlExportPlugin plugin;

    /**
     * The constructor
     */
    public HtmlExportPlugin() {
        // 
    }

    @Override
    public void start(BundleContext context) throws Exception {
        super.start(context);
        plugin = this;
    }

    @Override
    public void stop(BundleContext context) throws Exception {
        plugin = null;
        super.stop(context);
    }

    /**
     * Returns the shared instance
     * 
     * @return plugin the shared instance
     */
    public static HtmlExportPlugin getDefault() {
        return plugin;
    }

    /**
     * Returns an image descriptor for the image file at the given plug-in relative path
     * 
     * @param path the path
     * @return the image descriptor
     */
    public static ImageDescriptor getImageDescriptor(String path) {
        return imageDescriptorFromPlugin(PLUGIN_ID, path);
    }
}
